int _write(int fd, char *ptr, int len) {
    HAL_StatusTypeDef hstatus;

    if (fd == 1 || fd == 2) {
        hstatus = HAL_UART_Transmit(&huart1, (uint8_t*) ptr, len, HAL_MAX_DELAY);
        if (hstatus == HAL_OK)
            return len;
        else
            return -1;
    }
    return -1;
}

uint16_t crc16(uint8_t* a, uint8_t len){
  uint16_t crcVal=0;

  for(uint8_t i=0;i<len;i++){
    crcVal^=a[i];
    for(uint8_t j=0;j<8;j++){
      if(crcVal & 0x0001)
        crcVal=(crcVal>>1)^0xA001;
      else
        crcVal=(crcVal>>1);
    }
  }

  return crcVal;
}

void receiveChunk(){
	HAL_UART_Receive(&huart2, &startOfFrame, 1, HAL_MAX_DELAY);

	if(startOfFrame!=SOF){
		printf("No SOF message from ESP\r\n");
		return;
	}

	HAL_UART_Receive(&huart2, &chunkLen, 1, HAL_MAX_DELAY);
	HAL_UART_Receive(&huart2, receivedChunk, chunkLen, HAL_MAX_DELAY);
	HAL_UART_Receive(&huart2, (uint8_t*)&crc, 2, HAL_MAX_DELAY);

	uint16_t calculatedCRC=crc16(receivedChunk, chunkLen);

	if(calculatedCRC==crc){
		HAL_StatusTypeDef status=flashWrite(currentAddress, receivedChunk, chunkLen);

		if(status==HAL_OK){
			currentAddress+=chunkLen;
			receivedSize+=chunkLen;
		}

		printf("Chunk Written\r\n\n");

		uint8_t ackMessage=ACK;
		HAL_UART_Transmit(&huart2, &ackMessage, 1, HAL_MAX_DELAY);
	}else{
		uint8_t nackMessage=NACK;
		HAL_UART_Transmit(&huart2, &nackMessage, 1, HAL_MAX_DELAY);

		printf("NACK sent for some chunk number xxx\r\n");
		return;
	}

	return;

}

